    private void btnStkAdjusmentNewStockKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnStkAdjusmentNewStockKeyReleased
        // TODO add your handling code here:
        Pattern discountreg = Pattern.compile("^[0-9]*$");
        Matcher mdiscountreg = discountreg.matcher(btnStkAdjusmentNewStock.getText());
        
        if(!mdiscountreg.find()){
            JOptionPane.showMessageDialog(this, "You must entered numbers only!","Error",JOptionPane.ERROR_MESSAGE);
        }
        
        String oldStockString  = btnStkAdjusmentStockInHand.getText();
        String newStockString = btnStkAdjusmentNewStock.getText();
        
        if((btnStkAdjusmentNewStock.getText()).equals(""))
        {
            btnStkAdjusmentRemark.setText("");
        }
        
        if(!oldStockString.equals("") && !newStockString.equals(""))
        {
            String oStock[] = oldStockString.split("-");
            int oldStock = Integer.parseInt(oStock[1]);
            int newStock = Integer.parseInt(newStockString);
            if(oldStock<newStock)
            {
                btnStkAdjusmentRemark.setText("Stock Addition");
            }
            if(oldStock>newStock)
            {
                btnStkAdjusmentRemark.setText("Stock Deduction");
            }
        }               
    }//GEN-LAST:event_btnStkAdjusmentNewStockKeyReleased

    private void btnStkAdjusmentSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStkAdjusmentSaveActionPerformed
        // TODO add your handling code here:
        String oldStockString  = btnStkAdjusmentStockInHand.getText();
        String oStock[] = oldStockString.split("-");
        
        String dateString = btnStkAdjusmentDate.getText();
        String date[] = dateString.split(":");
        
        String itemNumberString = btnStkAdjusmentItemNo.getText();
        String itemNumber[] = itemNumberString.split("-");
        
        Database db = new Database();
        int i = db.insertUpdateDelete("INSERT INTO `tb_stockadjustment` VALUES (NULL, '"+itemNumber[1]+"', '"+date[1]+"', '"+btnStkAdjusmentRemark.getText()+"', '"+oStock[1]+"', '"+btnStkAdjusmentNewStock.getText()+"')");
        if(i==1)
        {
            int c = db.insertUpdateDelete("UPDATE `tb_item` SET `Qtyinhand` = '"+btnStkAdjusmentNewStock.getText()+"' WHERE `tb_item`.`INO` = "+itemNumber[1]+"");
            if(c==1)
            {
                User u = new User();                        
                u.recordUserOperation(username, "Item", "Stock Adjustment", btnStkAdjusmentItemNo.getText());
                JOptionPane.showMessageDialog(this, "Item Data Updated Successfully","Done!",JOptionPane.INFORMATION_MESSAGE);
                btnStkAdjusmentNewStock.setText("");
                btnStkAdjusmentRemark.setText("");
                try {
                    setItemData(itemNumber[1]);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Item Data Cannot loaded now repoen the menu","Error!",JOptionPane.ERROR_MESSAGE);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Item Data Not Updated Successfully","Done!",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Cannot Update Data Try Again","Error!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnStkAdjusmentSaveActionPerformed

