    public void setItemData(String itemNo) throws SQLException
    {
        Database db = new Database();
        ResultSet rs = db.selectData("select * from tb_item where ino = '"+itemNo+"'");
        while(rs.next())
        {
            btnStkAdjusmentItemNo.setText("Item Number-"+itemNo);
            btnStkAdjusmentItemName.setText("Item Name-"+rs.getString("IName"));
            btnStkAdjusmentStockInHand.setText("Stock in Hand-"+String.valueOf(rs.getInt("qtyinhand")));
        }
        DateTimeFormatter df = DateTimeFormatter.ofPattern("yyyy-MM-dd");  
        LocalDateTime now = LocalDateTime.now();
        btnStkAdjusmentDate.setText("Date:"+df.format(now));
    }
